package cube

import "testing"

func TestOrient(t *testing.T) {
	// @todo Change ALL cube strings to remove the 7 final characters denoting the xyz face orientations
	var orientExpect = map[Move]string{
		Xy: "0xz0xy0xy0xy0xz0xz0xz",
		Xz: "0xz0xy0xy0xy0xz0xz0xz",
		Xn: "0xy0xy0xy0xy0xy0xy0xy",
		Yx: "0xy0zy0xy0zy0xy0zy0zy",
		Yz: "0xy0zy0xy0zy0xy0zy0zy",
		Yn: "0xy0xy0xy0xy0xy0xy0xy",
		Zx: "0xy0xy0yx0yx0yx0xy0yx",
		Zy: "0xy0xy0yx0yx0yx0xy0yx",
		Zn: "0xy0xy0xy0xy0xy0xy0xy",
	}

	for move, expectation := range orientExpect {
		c := SolvedCube()
		c.reorient(move)
		if c.String() == expectation {
			continue
		}
		t.Error("During move", move, "we expected state", expectation, "and not", c.String())
	}

}
